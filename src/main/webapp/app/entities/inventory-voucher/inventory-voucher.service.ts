import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IInventoryVoucher } from '@/shared/model/inventory-voucher.model';

const baseApiUrl = 'api/inventory-vouchers';

export default class InventoryVoucherService {
  public find(id: number): Promise<IInventoryVoucher> {
    return new Promise<IInventoryVoucher>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IInventoryVoucher): Promise<IInventoryVoucher> {
    return new Promise<IInventoryVoucher>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IInventoryVoucher): Promise<IInventoryVoucher> {
    return new Promise<IInventoryVoucher>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
