import axios from 'axios';

import { IInventoryVoucherEntry } from '@/shared/model/inventory-voucher-entry.model';

const baseApiUrl = 'api/inventory-voucher-entries';

export default class InventoryVoucherEntryService {
  public find(id: number): Promise<IInventoryVoucherEntry> {
    return new Promise<IInventoryVoucherEntry>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IInventoryVoucherEntry): Promise<IInventoryVoucherEntry> {
    return new Promise<IInventoryVoucherEntry>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IInventoryVoucherEntry): Promise<IInventoryVoucherEntry> {
    return new Promise<IInventoryVoucherEntry>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
